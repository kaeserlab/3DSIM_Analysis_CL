% --- Executes on button press in pushbutton8.
function pushbutton8_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton8 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% batchprocess;
listfile=get(handles.edit28, 'string');  % getting info for analysis

[numbers, TEXT, list] = xlsread(listfile);
channellist1=list(:,1);
channellistr=list(:,2);
assignin('base','numbers',numbers);
assignin('base','TEXT',TEXT);
assignin('base','list',list);

filecheck=0;
m1=0;
mr=0;

for i=1:numel(channellist1) % check the existance of the files
    channel1=char(channellist1(i));
    channelr=char(channellistr(i));
    if exist(channel1,'file')~=2
        filecheck=filecheck+1;
        m1=m1+1;
        missingfile1(m1)=channellist1(i);
    end
    if exist(channelr,'file')~=2
        filecheck=filecheck+1;
        mr=mr+1;
        missingfiler(mr)=channellistr(i);
    end
end    
disp 'File checking done!' 

if filecheck>0 
    disp 'File not found!' 
    missingfile1
    missingfiler
end


if filecheck==0
    filenumber=numel(channellist1);
    set(handles.text47,'string',filenumber);
    pause(1);
    
    for i=1:numel(channellist1) % repeat the commands
        channel1=char(channellist1(i));
        channelr=char(channellistr(i));
        list_columnum=size(list,2);
        if list_columnum>2 % fill up the table
            for j=3:list_columnum
                fill=cell2mat(list(i,j));
                if isnan(fill)==0
                    if j==3
                       if get(handles.radiobutton1, 'value')==0
                           set(handles.edit30,'string',fill);
                       else
                           set(handles.edit4,'string',fill);
                       end
                    elseif j==4
                       set(handles.edit6,'string',fill); 
                    elseif j==5
                       set(handles.edit8,'string',fill); 
                    elseif j==6
                       if get(handles.radiobutton2, 'value')==0
                           set(handles.edit31,'string',fill);
                       else
                           set(handles.edit5,'string',fill);
                       end 
                    elseif j==7
                       set(handles.edit7,'string',fill);
                    elseif j==8
                       set(handles.edit9,'string',fill); 
                    end
                end
            end
        end
        
        set(handles.text34,'string',i);       
        set(handles.edit1,'string',channel1);
        set(handles.edit2,'string',channelr); 
        assignin('base','channel1',channel1);
        assignin('base','channelr',channelr);
        
        pause(1);
        pushbutton4_Callback(hObject, eventdata, handles)
        pushbutton5_Callback(hObject, eventdata, handles)
        
        close name 'distance histogram';
        pause(2);
        close name 'overlap';
        pause(2);
        close name 'object detection';
        pause(2);
        close name 'middle image'; % close figures
        pause(1);    
    end
end
disp 'All done!' 

